require 'spec_helper'

describe "StaticPages => " do

	describe "Page Content => " do
			{ home:    "Sample App", 
			  help:    "Help", 
			  about:   "About Us", 
			  contact: "Contact Us"
			}.each do |my_page,content|
		  it "#{my_page} should have the content #{content}" do
		  	visit "/static_pages/#{my_page}"
		  	expect(page).to have_content(content)
		  end
		end
	end

	describe "Page Title => " do
			{ :home => "Home", 
			  :help => "Help", 
			  :about => "About", 
			  :contact => "Contact"
			}.each do |my_page,my_title|
		  it "#{my_page} should have the title #{my_title}" do
		  	visit "/static_pages/#{my_page}"
		  	expect(page).to have_tilte("Ruby on Rails Tutorial Sample App | #{my_title}")
		  end
		end
	end
end